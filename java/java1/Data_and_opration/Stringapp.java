
public class Stringapp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); // String
		System.out.println('H'); // Character
		
		System.out.println("Hello" + "World");
		//New Line
		System.out.println("Hello \nWorld");
		//Hello "World"
		System.out.println("Hello \"World\"");
		
	}

}
